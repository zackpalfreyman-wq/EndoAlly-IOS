import SwiftUI
import PDFKit

// MARK: - Report Detail View
struct ReportDetailView: View {
    let reportType: ReportType
    let content: String?
    let profile: Profile?
    let symptoms: [Symptom]
    let medications: [HistoryMedication]
    let birthControls: [BirthControl]
    let treatments: [Treatment]
    let therapies: [Therapy]
    let familyHistory: [FamilyHistory]
    let onBack: () -> Void

    @State private var pdfURL: URL?
    @State private var showShareSheet = false
    @State private var isGeneratingPDF = false

    var body: some View {
        NavigationStack {
            ZStack {
                Color.warmWhite.ignoresSafeArea()
                ScrollView {
                    VStack(alignment: .leading, spacing: 20) {
                        // Header
                        VStack(alignment: .leading, spacing: 4) {
                            Text(reportType.label)
                                .font(.custom("DMSans-SemiBold", size: 24))
                                .foregroundColor(.charcoal)
                            Text("Generated \(Date().formatted(.dateTime.day().month(.wide).year()))")
                                .font(.custom("DMSans-Regular", size: 13))
                                .foregroundColor(.slateMid)
                        }

                        // Disclaimer
                        Text("This document is a self-reported symptom log generated by EndoAlly. It does not constitute a medical assessment, diagnosis, or clinical opinion.")
                            .font(.custom("DMSans-Regular", size: 12))
                            .foregroundColor(.slateMid)
                            .padding(12)
                            .background(Color.warmWhite)
                            .cornerRadius(8)
                            .overlay(RoundedRectangle(cornerRadius: 8).stroke(Color.appBorder, lineWidth: 1))

                        // AI content
                        if let aiContent = content {
                            MarkdownTextView(markdown: aiContent)
                        }

                        // Symptom Timeline
                        if reportType.id == "timeline" || reportType.id == "full" || content == nil {
                            if !symptoms.isEmpty {
                                VStack(alignment: .leading, spacing: 12) {
                                    Text("Symptom Timeline")
                                        .font(.custom("DMSans-SemiBold", size: 18))
                                        .foregroundColor(.charcoal)
                                    Divider()
                                    ForEach(buildTimeline(), id: \.name) { item in
                                        SymptomTimelineRow(item: item)
                                    }
                                }
                            }
                        }

                        // Medications
                        if (reportType.id == "medhistory" || reportType.id == "full" || reportType.id == "specialist") && !medications.isEmpty {
                            HistorySection(title: "Regular Medications") {
                                ForEach(medications) { med in
                                    HistoryRow(
                                        title: "\(med.name)\(med.dose.map { " — \($0)" } ?? "")",
                                        meta: [med.frequency, med.forWhat, med.stillTaking ? "Currently taking" : "No longer taking"]
                                            .compactMap { $0 }.joined(separator: " · ")
                                    )
                                }
                            }
                        }

                        // Birth Control
                        if (reportType.id == "medhistory" || reportType.id == "full" || reportType.id == "specialist") && !birthControls.isEmpty {
                            HistorySection(title: "Birth Control History") {
                                ForEach(birthControls) { bc in
                                    HistoryRow(
                                        title: bc.displayDescription,
                                        meta: bc.stillUsing ? "Currently using" : "Past"
                                    )
                                }
                            }
                        }

                        // Treatments
                        if (reportType.id == "medhistory" || reportType.id == "full" || reportType.id == "specialist") && !treatments.isEmpty {
                            HistorySection(title: "Past Treatments") {
                                ForEach(treatments) { t in
                                    HistoryRow(
                                        title: t.name,
                                        meta: [t.treatmentType, t.date, t.outcome].compactMap { $0 }.joined(separator: " · ")
                                    )
                                }
                            }
                        }

                        // Therapies
                        if (reportType.id == "medhistory" || reportType.id == "full") && !therapies.isEmpty {
                            HistorySection(title: "Therapies & Exercises") {
                                ForEach(therapies) { t in
                                    HistoryRow(
                                        title: t.name,
                                        meta: [t.therapyType, t.frequency].compactMap { $0 }.joined(separator: " · ")
                                    )
                                }
                            }
                        }

                        // Family History
                        if (reportType.id == "full" || reportType.id == "specialist") && !familyHistory.isEmpty {
                            HistorySection(title: "Family History") {
                                ForEach(familyHistory) { fh in
                                    HistoryRow(title: "\(fh.relation) — \(fh.condition)", meta: fh.notes ?? "")
                                }
                            }
                        }

                        // Export PDF button
                        Button(action: exportPDF) {
                            HStack {
                                if isGeneratingPDF {
                                    ProgressView().tint(.white)
                                } else {
                                    Image(systemName: "square.and.arrow.up")
                                    Text("Export PDF")
                                        .font(.custom("DMSans-Bold", size: 15))
                                }
                            }
                            .frame(maxWidth: .infinity)
                            .padding(.vertical, 14)
                            .background(Color.rose)
                            .foregroundColor(.white)
                            .cornerRadius(10)
                        }
                        .disabled(isGeneratingPDF)
                    }
                    .padding(.horizontal, 16)
                    .padding(.vertical, 20)
                }
            }
            .navigationBarTitleDisplayMode(.inline)
            .toolbar {
                ToolbarItem(placement: .cancellationAction) {
                    Button("Back", action: onBack).foregroundColor(.rose)
                }
            }
        }
        .sheet(isPresented: $showShareSheet) {
            if let url = pdfURL {
                ShareSheet(items: [url])
            }
        }
    }

    // MARK: - Timeline builder
    struct TimelineItem {
        let name: String
        let category: String
        let count: Int
        let avgSeverity: Double?
        let phases: [String]
        let firstDate: String
    }

    private func buildTimeline() -> [TimelineItem] {
        var map = [String: (count: Int, sevs: [Int], phases: Set<String>, first: String, cat: String)]()
        let fmt = DateFormatter()
        fmt.dateFormat = "yyyy-MM-dd'T'HH:mm:ssZ"
        let displayFmt = DateFormatter()
        displayFmt.dateFormat = "d MMM yyyy"
        displayFmt.locale = Locale(identifier: "en_AU")

        for s in symptoms {
            if map[s.symptom] == nil {
                map[s.symptom] = (0, [], Set(), s.loggedAt, s.category)
            }
            map[s.symptom]!.count += 1
            if let sev = s.severity { map[s.symptom]!.sevs.append(sev) }
            if let phase = s.phase { map[s.symptom]!.phases.insert(phase) }
            if s.loggedAt < map[s.symptom]!.first { map[s.symptom]!.first = s.loggedAt }
        }

        return map.map { (name, data) in
            let avgSev: Double? = data.sevs.isEmpty ? nil : Double(data.sevs.reduce(0, +)) / Double(data.sevs.count)
            let firstStr: String
            if let d = fmt.date(from: data.first) { firstStr = displayFmt.string(from: d) }
            else { firstStr = data.first }
            return TimelineItem(name: name, category: data.cat, count: data.count, avgSeverity: avgSev,
                                phases: Array(data.phases), firstDate: firstStr)
        }.sorted { $0.count > $1.count }
    }

    // MARK: - PDF export
    private func exportPDF() {
        isGeneratingPDF = true
        Task {
            let renderer = PDFRenderer(
                reportType: reportType,
                content: content,
                profile: profile,
                symptoms: symptoms,
                medications: medications,
                birthControls: birthControls,
                treatments: treatments,
                therapies: therapies,
                familyHistory: familyHistory
            )
            if let url = await renderer.render() {
                pdfURL = url
                showShareSheet = true
            }
            isGeneratingPDF = false
        }
    }
}

// MARK: - Markdown text view
struct MarkdownTextView: View {
    let markdown: String

    var body: some View {
        VStack(alignment: .leading, spacing: 8) {
            ForEach(parseBlocks(markdown), id: \.id) { block in
                switch block.type {
                case "h2":
                    Text(block.text)
                        .font(.custom("DMSans-SemiBold", size: 16))
                        .foregroundColor(.charcoal)
                        .padding(.top, 8)
                case "h3":
                    Text(block.text)
                        .font(.custom("DMSans-SemiBold", size: 14))
                        .foregroundColor(.charcoal)
                case "bullet":
                    HStack(alignment: .top, spacing: 8) {
                        Text("•")
                            .foregroundColor(.rose)
                        Text(block.text)
                            .font(.custom("DMSans-Regular", size: 14))
                            .foregroundColor(.charcoal)
                    }
                default:
                    if !block.text.hasPrefix("#") {
                        Text(block.text)
                            .font(.custom("DMSans-Regular", size: 14))
                            .foregroundColor(.charcoal)
                    }
                }
            }
        }
    }

    struct Block: Identifiable {
        let id = UUID()
        let type: String
        let text: String
    }

    private func parseBlocks(_ content: String) -> [Block] {
        content.components(separatedBy: "\n").compactMap { raw in
            let line = raw.trimmingCharacters(in: .whitespaces)
            if line.isEmpty { return nil }
            if line.hasPrefix("## ")  { return Block(type: "h2",   text: String(line.dropFirst(3))) }
            if line.hasPrefix("### ") { return Block(type: "h3",   text: String(line.dropFirst(4))) }
            if line.hasPrefix("- ") || line.hasPrefix("* ") { return Block(type: "bullet", text: String(line.dropFirst(2))) }
            return Block(type: "paragraph", text: line)
        }
    }
}

// MARK: - History Section
struct HistorySection<Content: View>: View {
    let title: String
    @ViewBuilder let content: () -> Content

    var body: some View {
        VStack(alignment: .leading, spacing: 10) {
            Text(title)
                .font(.custom("DMSans-SemiBold", size: 18))
                .foregroundColor(.charcoal)
            Divider()
            content()
        }
    }
}

// MARK: - History Row
struct HistoryRow: View {
    let title: String
    let meta: String

    var body: some View {
        VStack(alignment: .leading, spacing: 2) {
            Text(title)
                .font(.custom("DMSans-SemiBold", size: 14))
                .foregroundColor(.charcoal)
            if !meta.isEmpty {
                Text(meta)
                    .font(.custom("DMSans-Regular", size: 12))
                    .foregroundColor(.slateMid)
            }
        }
        .padding(.vertical, 6)
        .overlay(Divider(), alignment: .bottom)
    }
}

// MARK: - Symptom Timeline Row
struct SymptomTimelineRow: View {
    let item: ReportDetailView.TimelineItem

    var body: some View {
        HStack(alignment: .top) {
            VStack(alignment: .leading, spacing: 2) {
                Text(item.name)
                    .font(.custom("DMSans-SemiBold", size: 13))
                    .foregroundColor(.charcoal)
                Text(item.category)
                    .font(.custom("DMSans-Regular", size: 11))
                    .foregroundColor(.slateMid)
            }
            Spacer()
            VStack(alignment: .trailing, spacing: 2) {
                Text("×\(item.count)")
                    .font(.custom("DMSans-Bold", size: 13))
                    .foregroundColor(.rose)
                if let avg = item.avgSeverity {
                    Text(String(format: "%.1f/10", avg))
                        .font(.custom("DMSans-Regular", size: 11))
                        .foregroundColor(.slateMid)
                }
                Text(item.firstDate)
                    .font(.custom("DMSans-Regular", size: 11))
                    .foregroundColor(.slateMid)
            }
        }
        .padding(.vertical, 8)
        .overlay(Divider(), alignment: .bottom)
    }
}

// MARK: - Share Sheet
