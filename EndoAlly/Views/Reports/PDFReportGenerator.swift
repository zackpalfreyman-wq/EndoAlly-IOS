        UIActivityViewController(activityItems: items, applicationActivities: nil)
    }
    func updateUIViewController(_ vc: UIActivityViewController, context: Context) {}
}

// MARK: - PDF Renderer (PDFKit)
class PDFRenderer {
    let reportType: ReportType
    let content: String?
    let profile: Profile?
    let symptoms: [Symptom]
    let medications: [HistoryMedication]
    let birthControls: [BirthControl]
    let treatments: [Treatment]
    let therapies: [Therapy]
    let familyHistory: [FamilyHistory]

    init(reportType: ReportType, content: String?, profile: Profile?,
         symptoms: [Symptom], medications: [HistoryMedication], birthControls: [BirthControl],
         treatments: [Treatment], therapies: [Therapy], familyHistory: [FamilyHistory]) {
        self.reportType    = reportType
        self.content       = content
        self.profile       = profile
        self.symptoms      = symptoms
        self.medications   = medications
        self.birthControls = birthControls
        self.treatments    = treatments
        self.therapies     = therapies
        self.familyHistory = familyHistory
    }

    func render() async -> URL? {
        let pageRect = CGRect(x: 0, y: 0, width: 595, height: 842) // A4 points
        let renderer = UIGraphicsPDFRenderer(bounds: pageRect)

        let data = renderer.pdfData { ctx in
            ctx.beginPage()
            drawPage(ctx: ctx, pageRect: pageRect)
        }

        let tmpURL = FileManager.default.temporaryDirectory
            .appendingPathComponent("\(reportType.label.replacingOccurrences(of: " ", with: "_"))_\(Date().formatted(.dateTime.year().month().day())).pdf")
        try? data.write(to: tmpURL)
        return tmpURL
    }

    private func drawPage(ctx: UIGraphicsPDFRendererContext, pageRect: CGRect) {
        let margin: CGFloat = 50
        var y: CGFloat = margin
        let maxW = pageRect.width - margin * 2

        // Rose color
        let roseColor = UIColor(red: 0.659, green: 0.212, blue: 0.345, alpha: 1)
        let charcoalColor = UIColor(red: 0.102, green: 0.153, blue: 0.22, alpha: 1)
        let slateColor = UIColor(red: 0.333, green: 0.376, blue: 0.439, alpha: 1)

        // Header logo
        let logoAttr: [NSAttributedString.Key: Any] = [
            .font: UIFont.systemFont(ofSize: 22, weight: .regular),
            .foregroundColor: charcoalColor
        ]
        let logoAllyAttr: [NSAttributedString.Key: Any] = [
            .font: UIFont.systemFont(ofSize: 22, weight: .bold),
            .foregroundColor: roseColor
        ]
        let endoStr = NSAttributedString(string: "Endo", attributes: logoAttr)
        let allyStr = NSAttributedString(string: "Ally", attributes: logoAllyAttr)
        let combined = NSMutableAttributedString()
        combined.append(endoStr)
        combined.append(allyStr)
        combined.draw(at: CGPoint(x: margin, y: y))

        // Date top right
        let dateStr = Date().formatted(.dateTime.day().month(.wide).year())
        let dateAttr: [NSAttributedString.Key: Any] = [.font: UIFont.systemFont(ofSize: 9), .foregroundColor: slateColor]
        let dateNS = NSAttributedString(string: "Generated \(dateStr)", attributes: dateAttr)
        let dateSize = dateNS.size()
        dateNS.draw(at: CGPoint(x: pageRect.width - margin - dateSize.width, y: y + 6))

        y += 32

        // Rose divider
        roseColor.setFill()
        UIBezierPath(rect: CGRect(x: margin, y: y, width: maxW, height: 1.5)).fill()
        y += 10

        // Disclaimer
        let disclaimerAttr: [NSAttributedString.Key: Any] = [
            .font: UIFont.systemFont(ofSize: 8),
            .foregroundColor: slateColor
        ]
        let disclaimer = "This document is a self-reported symptom log generated by EndoAlly. It does not constitute a medical assessment, diagnosis, or clinical opinion. It has been prepared to assist you in communicating your experiences to a qualified Australian healthcare provider."
        let disclaimerNS = NSAttributedString(string: disclaimer, attributes: disclaimerAttr)
        let disclaimerRect = CGRect(x: margin, y: y, width: maxW, height: 40)
        disclaimerNS.draw(in: disclaimerRect)
        y += 36

        // Title
        let titleAttr: [NSAttributedString.Key: Any] = [
            .font: UIFont.systemFont(ofSize: 20, weight: .bold),
            .foregroundColor: charcoalColor
        ]
        NSAttributedString(string: reportType.label, attributes: titleAttr).draw(at: CGPoint(x: margin, y: y))
        y += 28

        // Patient meta
        if let p = profile {
            let metaParts = [p.name, p.age.map { "Age \($0)" }, p.cycleLength.map { "Cycle: \($0) days" }].compactMap { $0 }
            let metaAttr: [NSAttributedString.Key: Any] = [.font: UIFont.systemFont(ofSize: 10), .foregroundColor: slateColor]
            NSAttributedString(string: metaParts.joined(separator: "  ·  "), attributes: metaAttr).draw(at: CGPoint(x: margin, y: y))
            y += 20
        }

        y += 10

        // AI content
        if let aiContent = content {
            for line in aiContent.components(separatedBy: "\n") {
                if y > pageRect.height - 80 { ctx.beginPage(); y = margin }
                let trimmed = line.trimmingCharacters(in: .whitespaces)
                if trimmed.isEmpty { y += 6; continue }

                let isH2     = trimmed.hasPrefix("## ")
                let isH3     = trimmed.hasPrefix("### ")
                let isBullet = trimmed.hasPrefix("- ") || trimmed.hasPrefix("* ")

                let text: String
                let font: UIFont
                if isH2 {
                    text = String(trimmed.dropFirst(3))
                    font = UIFont.systemFont(ofSize: 13, weight: .bold)
                    y += 8
                } else if isH3 {
                    text = String(trimmed.dropFirst(4))
                    font = UIFont.systemFont(ofSize: 11, weight: .semibold)
                } else if isBullet {
                    text = "• " + String(trimmed.dropFirst(2))
                    font = UIFont.systemFont(ofSize: 10)
                } else {
                    text = trimmed
                    font = UIFont.systemFont(ofSize: 10)
                }

                let attr: [NSAttributedString.Key: Any] = [.font: font, .foregroundColor: charcoalColor]
                let ns = NSAttributedString(string: text, attributes: attr)
                let h = ns.boundingRect(with: CGSize(width: maxW, height: 1000), options: .usesLineFragmentOrigin, context: nil).height
                ns.draw(in: CGRect(x: margin + (isBullet ? 8 : 0), y: y, width: maxW, height: h + 4))
                y += h + 4
            }
            y += 12
        }

        // Symptoms section
        if (reportType.id == "timeline" || reportType.id == "full" || content == nil) && !symptoms.isEmpty {
            if y > pageRect.height - 80 { ctx.beginPage(); y = margin }
            drawSectionHeader("Symptom Timeline", y: y, margin: margin, maxW: maxW, color: charcoalColor)
            y += 24

            let headerAttr: [NSAttributedString.Key: Any] = [.font: UIFont.systemFont(ofSize: 9, weight: .bold), .foregroundColor: UIColor.white]
            UIColor(red: 0.659, green: 0.212, blue: 0.345, alpha: 1).setFill()
            UIBezierPath(roundedRect: CGRect(x: margin, y: y, width: maxW, height: 20), cornerRadius: 3).fill()
            NSAttributedString(string: "Symptom", attributes: headerAttr).draw(at: CGPoint(x: margin + 8, y: y + 5))
            NSAttributedString(string: "Category", attributes: headerAttr).draw(at: CGPoint(x: margin + 180, y: y + 5))
            NSAttributedString(string: "Count", attributes: headerAttr).draw(at: CGPoint(x: margin + 320, y: y + 5))
            NSAttributedString(string: "Avg Sev", attributes: headerAttr).draw(at: CGPoint(x: margin + 380, y: y + 5))
            y += 22

            for (i, s) in symptoms.prefix(40).enumerated() {
                if y > pageRect.height - 60 { ctx.beginPage(); y = margin }
                if i % 2 == 1 {
                    UIColor(red: 0.973, green: 0.965, blue: 0.957, alpha: 1).setFill()
                    UIBezierPath(rect: CGRect(x: margin, y: y, width: maxW, height: 18)).fill()
                }
                let rowAttr: [NSAttributedString.Key: Any] = [.font: UIFont.systemFont(ofSize: 9), .foregroundColor: charcoalColor]
                NSAttributedString(string: s.symptom, attributes: rowAttr).draw(at: CGPoint(x: margin + 8, y: y + 4))
                NSAttributedString(string: s.category, attributes: rowAttr).draw(at: CGPoint(x: margin + 180, y: y + 4))
                let sevStr = s.severity.map { "\($0)/10" } ?? "—"
                NSAttributedString(string: sevStr, attributes: rowAttr).draw(at: CGPoint(x: margin + 380, y: y + 4))
                y += 18
            }
            y += 12
        }
    }

    private func drawSectionHeader(_ title: String, y: CGFloat, margin: CGFloat, maxW: CGFloat, color: UIColor) {
        let attr: [NSAttributedString.Key: Any] = [.font: UIFont.systemFont(ofSize: 14, weight: .semibold), .foregroundColor: color]
        NSAttributedString(string: title, attributes: attr).draw(at: CGPoint(x: margin, y: y))
        UIColor(red: 0.682, green: 0.643, blue: 0.604, alpha: 1).setFill()
        UIBezierPath(rect: CGRect(x: margin, y: y + 20, width: maxW, height: 0.75)).fill()
    }
}
